async function main() {
  const test = await prisma.test.upsert({
    where: { type: TestType.SHMISHEK },
    update: {
      title: TEST_TITLE,
      description: TEST_DESCRIPTION,
      isActive: true
    },
    create: {
      type: TestType.SHMISHEK,
      title: TEST_TITLE,
      description: TEST_DESCRIPTION,
      isActive: true
    }
  });

  await prisma.testQuestion.deleteMany({
    where: { testId: test.id }
  });

  await prisma.testQuestion.createMany({
    data: QUESTIONS.map(q => ({
      testId: test.id,
      index: q.index,
      text: q.text
    }))
  });

  await prisma.testScale.deleteMany({
    where: { testId: test.id }
  });

  await prisma.testScale.createMany({
    data: SCALES.map(s => ({
      testId: test.id,
      key: s.key,
      title: s.title,
      description: s.description
    }))
  });

  console.log("Тест Шмишека (детский вариант, 88 вопросов) записан в БД, testId =", test.id);
}

main()
  .catch(e => {
    console.error(e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
