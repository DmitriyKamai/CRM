// Логика опросника Шмишека:
// - расчёт баллов по шкалам
// - текстовая интерпретация результатов
//
// Вопросы и точные формулировки шкал зависят от используемой версии методики.
// В реальном проекте важно использовать утверждённый вариант опросника
// и согласовать права на его использование.

export type ShmishekScaleKey =
  | "hyperthymic"
  | "dysthymic"
  | "cyclothymic"
  | "exalted"
  | "anxious"
  | "emotive"
  | "demonstrative"
  | "stuck"
  | "excitable"
  | "pedantic";

export type ShmishekScores = Record<ShmishekScaleKey, number>;

// Пример простой схемы ответов:
// значение от 0 до 3 (0 – «совсем не про меня», 3 – «очень про меня»).
export type ShmishekAnswerValue = 0 | 1 | 2 | 3;

export interface ShmishekAnswerMap {
  // ключ – индекс вопроса (1..N), значение – выбранный балл
  [questionIndex: number]: ShmishekAnswerValue;
}

// Упрощённая карта связи вопросов и шкал.
// В реальной методике каждый вопрос относится к одной или нескольким шкалам.
// Здесь мы задаём небольшую демонстрационную структуру; её можно расширять.
const questionScaleMap: Record<number, ShmishekScaleKey> = {
  1: "hyperthymic",
  2: "dysthymic",
  3: "cyclothymic",
  4: "exalted",
  5: "anxious",
  6: "emotive",
  7: "demonstrative",
  8: "stuck",
  9: "excitable",
  10: "pedantic"
  // ...дальше по мере добавления вопросов
};

export function computeShmishekScores(
  answers: ShmishekAnswerMap
): ShmishekScores {
  const scores: ShmishekScores = {
    hyperthymic: 0,
    dysthymic: 0,
    cyclothymic: 0,
    exalted: 0,
    anxious: 0,
    emotive: 0,
    demonstrative: 0,
    stuck: 0,
    excitable: 0,
    pedantic: 0
  };

  for (const [key, value] of Object.entries(answers)) {
    const index = Number(key);
    const scale = questionScaleMap[index];
    if (!scale) continue;
    scores[scale] += value ?? 0;
  }

  return scores;
}

function interpretScale(key: ShmishekScaleKey, value: number): string {
  // Грубая трёхуровневая интерпретация.
  // Пороговые значения зависят от числа вопросов на шкалу;
  // здесь используются условные значения, которые можно калибровать.
  let level: "low" | "medium" | "high" = "medium";

  if (value <= 4) level = "low";
  else if (value >= 9) level = "high";

  switch (key) {
    case "hyperthymic":
      if (level === "low")
        return "Гипертимные черты выражены слабо: человек, как правило, сдержан в проявлении эмоций и активности.";
      if (level === "high")
        return "Ярко выраженные гипертимные черты: высокий уровень активности, оптимизма, потребность в общении и новых впечатлениях.";
      return "Умеренные гипертимные черты: человек активен и общителен, но сохраняет способность к самоконтролю и отдыху.";
    case "dysthymic":
      if (level === "low")
        return "Дистимные черты практически не проявляются: фон настроения в целом ровный и устойчивый.";
      if (level === "high")
        return "Повышенная склонность к снижению настроения, пессимистическим оценкам и переживанию трудностей.";
      return "Умеренные дистимные черты: в периоды нагрузки возможны эпизоды снижения настроения и утомляемости.";
    case "cyclothymic":
      if (level === "low")
        return "Циклотимные колебания настроения выражены незначительно, эмоциональный фон скорее стабилен.";
      if (level === "high")
        return "Выраженные циклотимные колебания: возможны чередования подъёма и спада настроения, энергии и мотивации.";
      return "Умеренные циклотимные черты: эмоциональный фон может меняться, но человек в целом сохраняет контроль и адаптивность.";
    case "exalted":
      if (level === "low")
        return "Черты экзальтированности слабо выражены: реакции на события чаще спокойные и сдержанные.";
      if (level === "high")
        return "Ярко выраженная экзальтированность: сильные эмоциональные реакции на значимые события, впечатлительность и вдохновляемость.";
      return "Умеренная экзальтированность: человек эмоционально отзывчив, но способен регулировать проявления.";
    case "anxious":
      if (level === "low")
        return "Тревожность выражена минимально: человек, как правило, уверенно действует в новых и неопределённых ситуациях.";
      if (level === "high")
        return "Повышенная тревожность: склонность к переживаниям, сомнениям, ожиданию неблагоприятных исходов.";
      return "Умеренная тревожность: в сложных ситуациях возможны сомнения и переживания, но они не полностью мешают действию.";
    case "emotive":
      if (level === "low")
        return "Эмотивные (чувствительные) черты выражены слабо: эмоциональные реакции более рационально окрашены.";
      if (level === "high")
        return "Выраженная чувствительность и эмпатия, глубокое переживание отношений и событий, возможная ранимость.";
      return "Умеренная эмоциональная чувствительность: человек сопереживает другим, но способен сохранять дистанцию при необходимости.";
    case "demonstrative":
      if (level === "low")
        return "Демонстративные черты практически отсутствуют: человек не стремится к привлечению внимания любой ценой.";
      if (level === "high")
        return "Выраженная потребность в признании и внимании, склонность к самопрезентации и иногда театральности поведения.";
      return "Умеренная демонстративность: человеку важно быть замеченным и оценённым, но это не всегда доминирует в поведении.";
    case "stuck":
      if (level === "low")
        return "Черты застревания выражены слабо: человек относительно легко отпускает прошлые обиды и неприятные события.";
      if (level === "high")
        return "Склонность «застревать» на значимых переживаниях, обидах и идеях, что может усиливать напряжение в отношениях.";
      return "Умеренное застревание: человек чувствителен к несправедливости, но в большинстве случаев способен со временем отпустить ситуацию.";
    case "excitable":
      if (level === "low")
        return "Возбудимость низкая: самоконтроль и выдержка обычно сохраняются даже в сложных ситуациях.";
      if (level === "high")
        return "Повышенная возбудимость, импульсивность реакции, возможны вспышки раздражения при сильном напряжении.";
      return "Умеренная возбудимость: в повседневности человек сдержан, но при перегрузке возможны более резкие реакции.";
    case "pedantic":
      if (level === "low")
        return "Педантичность выражена незначительно: человек достаточно гибок к изменениям и неточностям.";
      if (level === "high")
        return "Выраженная педантичность: стремление к порядку, точности и контролю, что может затруднять гибкость и спонтанность.";
      return "Умеренная педантичность: склонность планировать и доводить дела до конца при сохранении некоторой гибкости.";
  }
}

export function buildShmishekInterpretation(scores: ShmishekScores): string {
  const parts: string[] = [];

  (Object.entries(scores) as [ShmishekScaleKey, number][]).forEach(
    ([key, value]) => {
      const text = interpretScale(key, value);
      const label = scaleLabel(key);
      parts.push(`${label}: ${text}`);
    }
  );

  return parts.join("\n\n");
}

export function scaleLabel(key: ShmishekScaleKey): string {
  switch (key) {
    case "hyperthymic":
      return "Гипертимный тип";
    case "dysthymic":
      return "Дистимный тип";
    case "cyclothymic":
      return "Циклотимный тип";
    case "exalted":
      return "Экзальтированный тип";
    case "anxious":
      return "Тревожный тип";
    case "emotive":
      return "Эмотивный тип";
    case "demonstrative":
      return "Демонстративный тип";
    case "stuck":
      return "Застревающий тип";
    case "excitable":
      return "Возбудимый тип";
    case "pedantic":
      return "Педантичный тип";
  }
}

