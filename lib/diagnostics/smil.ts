/**
 * СМИЛ — Стандартизированный многофакторный метод исследования личности (Л.Н. Собчик).
 * Ключи и нормы: Psylab.info. Ответы: «верно» = 1, «неверно» = 0.
 * Подсчёт сырых баллов, K-коррекция, перевод в T-баллы по половым нормам.
 */

export type SmilScaleKey =
  | "L"
  | "F"
  | "K"
  | "1"  // Hs — Сверхконтроль
  | "2"  // D  — Пессимистичность
  | "3"  // Hy — Эмоц. лабильность
  | "4"  // Pd — Импульсивность
  | "5"  // Mf — Женственность
  | "6"  // Pa — Ригидность
  | "7"  // Pt — Тревожность
  | "8"  // Sc — Индивидуалистичность
  | "9"  // Ma — Оптимистичность
  | "0"; // Si — Интроверсия

export type SmilScores = Record<SmilScaleKey, number>;

/** Ответ: 0 = «неверно», 1 = «верно». */
export type SmilAnswerValue = 0 | 1;

export interface SmilAnswerMap {
  [questionIndex: number]: SmilAnswerValue;
}

