function rawForScale(
  scale: SmilScaleKey,
  answers: SmilAnswerMap,
  variant: SmilVariant
): number {
  const keys =
    scale === "5"
      ? (variant === "female" ? BASE_KEYS_5_FEMALE : BASE_KEYS_5_MALE)
      : BASE_KEYS[scale];
  const { direct, reverse } = keys;
  let count = 0;
  for (const i of direct) if (answers[i] === 1) count++;
  for (const i of reverse) if (answers[i] === 0) count++;
  return count;
}

/** Сырые баллы по базисным шкалам (до K-коррекции). Для женщин по шкале 5 используется ключ ММ (Psylab). */
export function computeSmilRawScores(
  answers: SmilAnswerMap,
  variant: SmilVariant
): Record<SmilScaleKey, number> {
  const raw: Record<SmilScaleKey, number> = {} as Record<SmilScaleKey, number>;
  const keys = Object.keys(BASE_KEYS) as SmilScaleKey[];
  for (const k of keys) {
    raw[k] = rawForScale(k, answers, variant);
  }
  return raw;
}

/** K-коррекция: Hs += 0.5*K, Pd += 0.4*K, Pt += K, Sc += K, Ma += 0.2*K. Остальные без изменений. */
function applyKCorrection(raw: Record<SmilScaleKey, number>): Record<SmilScaleKey, number> {
  const K = raw.K;
  return {
    ...raw,
    "1": raw["1"] + 0.5 * K,
    "4": raw["4"] + 0.4 * K,
    "7": raw["7"] + K,
    "8": raw["8"] + K,
    "9": raw["9"] + 0.2 * K
  };
}

function rawToT(raw: number, m: number, sigma: number): number {
  if (sigma <= 0) return 50;
  const t = 50 + (10 * (raw - m)) / sigma;
  return Math.round(Math.max(0, Math.min(120, t)));
}

export type SmilVariant = "male" | "female";

/** T-баллы по базисным шкалам (после K-коррекции). */
export function computeSmilTScores(
  answers: SmilAnswerMap,
  variant: SmilVariant
): SmilScores {
  const raw = computeSmilRawScores(answers, variant);
  const corrected = applyKCorrection(raw);
  const result: SmilScores = {} as SmilScores;
  const keys = Object.keys(T_NORMS) as SmilScaleKey[];
  for (const k of keys) {
    const norm = T_NORMS[k][variant];
    result[k] = rawToT(corrected[k], norm.m, norm.sigma);
  }
  return result;
}

