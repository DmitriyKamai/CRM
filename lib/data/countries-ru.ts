/** ISO-коды стран бывшего СССР (без РФ и Беларуси — они выводятся первыми) */
const POST_SOVIET_ISO = new Set([
  "AM", "AZ", "EE", "GE", "KZ", "KG", "LV", "LT", "MD", "TJ", "TM", "UA", "UZ"
]);

/** Все страны: РФ и Беларусь вверху, затем постсоветские по алфавиту, затем остальные по алфавиту */
export const COUNTRIES_RU: { name: string; isoCode: string }[] = (() => {
  const list = Object.entries(RU_NAMES).map(([isoCode, name]) => ({ name, isoCode }));
  const ru = list.find((c) => c.isoCode === "RU")!;
  const by = list.find((c) => c.isoCode === "BY")!;
  const postSoviet = list
    .filter((c) => POST_SOVIET_ISO.has(c.isoCode))
    .sort((a, b) => a.name.localeCompare(b.name, "ru"));
  const rest = list
    .filter((c) => c.isoCode !== "RU" && c.isoCode !== "BY" && !POST_SOVIET_ISO.has(c.isoCode))
    .sort((a, b) => a.name.localeCompare(b.name, "ru"));
  return [by, ru, ...postSoviet, ...rest];
})();

/** Старые названия для обратной совместимости (уже сохранённые в БД) */
const LEGACY_COUNTRY_NAMES: Record<string, string> = {
  "Республика Беларусь": "BY",
  "Российская Федерация": "RU"
};

export function getCountryCodeByName(name: string): string | null {
  const trimmed = name?.trim();
  if (!trimmed) return null;
  const found = COUNTRIES_RU.find((c) => c.name === trimmed);
  if (found) return found.isoCode;
  return LEGACY_COUNTRY_NAMES[trimmed] ?? null;
}
